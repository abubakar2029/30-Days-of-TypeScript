type JSONValue5 =
  | null
  | boolean
  | number
  | string
  | JSONValue5[]
  | { [key: string]: JSONValue5 };
type Fn5 = (...args: JSONValue5[]) => void;

function cancellable(fn: Fn5, args: JSONValue5[], t: number): Function {
  fn(...args);
  const timerid = setInterval(() => fn(...args), t);
  return function name() {
    clearInterval(timerid);
  };
}

